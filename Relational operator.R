#Logical operator

var1=c(FALSE,TRUE,FALSE,TRUE)
var2=c(TRUE,TRUE,FALSE,FALSE)
#AND
print(var1 & var2)
#OR
print(var1 | var2)

#actual AND--->compare first element and decide True or false
print(var1 && var1)

#actual OR
print(var1 || var2)

#NOT operator !
print(!var1)
print(!var2)


